int deplacement(int i, int j, int x, int y, int table[][])
{
    if (table[x][y + 1] == 1)
    {
        table[x][y] = 3;
        printf("B"); // BAS
    }
    else
    {
        if (table[x][y - 1])
        {
            table[x][y] = 4;
            printf("H"); // HAUT
        }
        else
        {
            if (table[x + 1][y] == 1)
            {
                table[x][y] = 5;
                printf("D");//DROITE
            }
            else
            {
                if (table[x - 1][y] == 1)
                {
                    table[x][y] = 6;
                    printf("G");//GAUCHE
                }
            }
        }
    }
}

